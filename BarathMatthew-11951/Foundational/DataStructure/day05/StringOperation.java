package com.matthew.day05;
import java.util.TreeSet;
public class StringOperation {

	public static void main(String[] args) {
		TreeSet<String> list = new TreeSet<String>();
		list.add("Apple");
		list.add("Orange");
		list.add("Grapes");
		list.add("Banana");
		list.add("Peach");
		list.add("Leamon");
		list.add("Avacado");
		list.add("Kivi");
		list.add("$Apple");
		list.add("#Apple");
		list.add("0Apple");
		list.add("1Apple");
		list.add("apple");
		System.out.println(list);
	}

}
