package com.jeban.day5;
import java.util.TreeMap;

public class TreeMap6 {

	public static void main(String[] args) {
		TreeMap<Character,Integer> map=new TreeMap<Character,Integer>();
		
		
		map.put('A',(int)'A');
		map.put('B',(int)'B');
		map.put('C',(int)'C');
		map.put('D',(int)'D');
		map.put('Z',(int)'Z');
		map.put('H',(int)'H');
		map.put('U',(int)'U');
		
		System.out.println(map);

	}

}
