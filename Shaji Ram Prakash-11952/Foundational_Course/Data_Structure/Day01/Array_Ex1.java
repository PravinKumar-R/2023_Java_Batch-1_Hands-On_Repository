package com.shaji.Day01;

public class Array_Ex1 { // Displaying Elements with Array

	public static void main(String[] args) {
		int arr[] = {10, 20, 30, 40, 50 };
		
		System.out.println("Third Element : " + arr[2]); //Printing the Element
		
		arr[2] = 24; //Updating the Element
		
		System.out.println("New Element : " + arr[2]);


	}

}
